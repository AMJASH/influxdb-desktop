package website.yuanhui.model.log;

public class LogInfo {
    private String msg;
    private Exception e;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Exception getE() {
        return e;
    }

    public void setE(Exception e) {
        this.e = e;
    }


}
